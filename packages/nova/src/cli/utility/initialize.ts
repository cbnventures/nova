  /**
   * CLI - Utility - Initialize - Prompt With Cancel.
   *
   * Thin wrapper around the prompts library that detects user cancellation (Ctrl-C) and
   * returns a typed discriminated union of result.
   *
   * @param {Cli_Utility_Initialize_Runner_PromptWithCancel_Questions} questions - Questions.
   *
   * @private
   *
   * @returns {Cli_Utility_Initialize_Runner_PromptWithCancel_Returns}
   *
   * @since 0.11.0
   */
  private static async promptWithCancel<Keys extends string, Result>(questions: Cli_Utility_Initialize_Runner_PromptWithCancel_Questions<Keys>): Cli_Utility_Initialize_Runner_PromptWithCancel_Returns<Keys, Result> {
    let cancelled: Cli_Utility_Initialize_Runner_PromptWithCancel_Cancelled = false;

    const result: Cli_Utility_Initialize_Runner_PromptWithCancel_Result<Keys, Result> = await prompts<Keys>(questions, {
      onCancel: () => false,
    });

    if (Object.keys(result).length === 0) {
      cancelled = true;
    }

    if (cancelled === true) {
      return {
        cancelled: true,
      };
    }

    return {
      cancelled: false,
      result,
    };
  }

  /**
   * CLI - Utility - Initialize - Check Path.
   *
   * Guards the run method by traversing backward
   * from the working directory for package.json files.
   * Returns true only when one exists at cwd.
   *
   * @param {Cli_Utility_Initialize_Runner_CheckPath_CurrentDirectory} currentDirectory - Current directory.
   *
   * @private
   *
   * @returns {Cli_Utility_Initialize_Runner_CheckPath_Returns}
   *
   * @since 0.11.0
   */
  private static async checkPath(currentDirectory: Cli_Utility_Initialize_Runner_CheckPath_CurrentDirectory): Cli_Utility_Initialize_Runner_CheckPath_Returns {
    const locations: Cli_Utility_Initialize_Runner_CheckPath_Locations = await discoverPathsWithFile('package.json', 'backward');

    Logger.customize({
      name: 'Runner.checkPath',
      purpose: 'detectedLocations',
    }).debug(locations);

    // If command was ran outside of project root directory.
    if (locations.length < 1) {
      const lessThanOneMessage: Cli_Utility_Initialize_Runner_CheckPath_LessThanOneMessage = [
        'No "package.json" files were found. Re-run this command inside the project root directory.',
        `Current directory is "${currentDirectory}"`,
      ].join('\n');

      Logger.customize({
        name: 'Runner.checkPath',
        purpose: 'lessThanOne',
      }).error(lessThanOneMessage);

      return false;
    }

    // If command was ran inside a monorepo package.
    if (locations.length > 1) {
      const greaterThanOneMessage: Cli_Utility_Initialize_Runner_CheckPath_GreaterThanOneMessage = [
        'Multiple "package.json" files were found. Re-run this command inside the project root directory.',
        `Current directory is "${currentDirectory}"`,
      ].join('\n');

      Logger.customize({
        name: 'Runner.checkPath',
        purpose: 'greaterThanOne',
      }).error(greaterThanOneMessage);

      return false;
    }

    // If command was ran outside the project root directory.
    if (locations.length === 1 && locations[0] !== currentDirectory) {
      const notProjectRootDirectoryMessage: Cli_Utility_Initialize_Runner_CheckPath_NotProjectRootDirectoryMessage = [
        'Must be run inside the project root directory.',
        `Current directory is "${currentDirectory}"`,
      ].join('\n');

      Logger.customize({
        name: 'Runner.checkPath',
        purpose: 'notProjectRootDirectory',
      }).error(notProjectRootDirectoryMessage);

      return false;
    }

    return true;
  }

