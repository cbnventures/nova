  /**
   * CLI - Utility - Initialize - Normalize Workspace Name.
   *
   * Enforces naming rules per workspace role.
   * Config, app, and tool roles require a prefixed slug
   * while package roles accept scoped names.
   *
   * @param {Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_Value} value - Value.
   * @param {Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_Role}  role  - Role.
   * @param {Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_Base}  base  - Base.
   *
   * @private
   *
   * @returns {Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_Returns}
   *
   * @since 0.11.0
   */
  private static normalizeWorkspaceName(value: Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_Value, role: Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_Role, base: Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_Base): Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_Returns {
    if (typeof value !== 'string') {
      return {
        result: `Unexpected type error. Expect type to be "string", got "${typeof value}".`,
        sanitized: undefined,
      };
    }

    const trimmedValue: Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_TrimmedValue = value.trim();

    if (trimmedValue === '') {
      return {
        result: 'Enter a package name.',
        sanitized: undefined,
      };
    }

    switch (role) {
      case 'config':
      case 'app':
      case 'tool': {
        // Base for "config", "app", and "tool" is either `${projectSlug}-${role}` or just `${role}`.
        const expectedPrefix: Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_ExpectedPrefix = `${base}-`;

        if (trimmedValue.startsWith(expectedPrefix) === false) {
          return {
            result: `Begin with "${expectedPrefix}" and add a descriptor slug.`,
            sanitized: undefined,
          };
        }

        const descriptor: Cli_Utility_Initialize_Runner_NormalizeWorkspaceName_Descriptor = trimmedValue.slice(expectedPrefix.length);

        if (descriptor.length === 0) {
          return {
            result: 'Add a descriptor after the prefix.',
            sanitized: undefined,
          };
        }

        if (LIB_REGEX_PATTERN_SLUG_SIMPLE.test(descriptor) === false) {
          return {
            result: 'Descriptor must match the slug pattern (lowercase letters, numbers, hyphens, underscores).',
            sanitized: undefined,
          };
        }

        return {
          result: true,
          sanitized: trimmedValue,
        };
      }

      case 'template':
      case 'package':
      default: {
        if (LIB_REGEX_PATTERN_SLUG_SIMPLE.test(trimmedValue) === true || LIB_REGEX_PATTERN_SLUG_SCOPED.test(trimmedValue) === true) {
          return {
            result: true,
            sanitized: trimmedValue,
          };
        }

        return {
          result: 'Enter an unscoped slug or a scoped package name (e.g. @scope/name).',
          sanitized: undefined,
        };
      }
    }
  }
