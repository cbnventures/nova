  /**
   * CLI - Utility - Initialize - Normalize URL.
   *
   * Parses and validates a single URL string, checking its protocol against a whitelist
   * determined by the protocol mode (generic or repository).
   *
   * @param {Cli_Utility_Initialize_Runner_NormalizeUrl_Value}    value    - Value.
   * @param {Cli_Utility_Initialize_Runner_NormalizeUrl_Protocol} protocol - Protocol.
   *
   * @private
   *
   * @returns {Cli_Utility_Initialize_Runner_NormalizeUrl_Returns}
   *
   * @since 0.11.0
   */
  private static normalizeUrl(value: Cli_Utility_Initialize_Runner_NormalizeUrl_Value, protocol: Cli_Utility_Initialize_Runner_NormalizeUrl_Protocol): Cli_Utility_Initialize_Runner_NormalizeUrl_Returns {
    if (typeof value !== 'string') {
      return {
        result: `Unexpected type error. Expect type to be "string", got "${typeof value}".`,
        sanitized: undefined,
      };
    }

    const trimmedValue: Cli_Utility_Initialize_Runner_NormalizeUrl_TrimmedValue = value.trim();

    // Allow blanks.
    if (trimmedValue === '') {
      return {
        result: true,
        sanitized: undefined,
      };
    }

    const rules: Cli_Utility_Initialize_Runner_NormalizeUrl_Rules = {
      generic: {
        allowed: [
          'http:',
          'https:',
        ],
        message: 'Enter a valid generic URL (e.g., https://) or leave blank.',
      },
      repository: {
        allowed: [
          'git:',
          'git+https:',
          'git+ssh:',
          'git+http:',
          'http:',
          'https:',
        ],
        message: 'Enter a valid repository URL (e.g., git+https://) or leave blank.',
      },
    };

    const allowed: Cli_Utility_Initialize_Runner_NormalizeUrl_Allowed = (protocol === 'repository') ? rules['repository']['allowed'] : rules['generic']['allowed'];
    const errorMessage: Cli_Utility_Initialize_Runner_NormalizeUrl_ErrorMessage = (protocol === 'repository') ? rules['repository']['message'] : rules['generic']['message'];

    try {
      const url: Cli_Utility_Initialize_Runner_NormalizeUrl_Url = new URL(trimmedValue);

      if (allowed.includes(url.protocol) === true) {
        return {
          result: true,
          sanitized: url.toString(),
        };
      }
    } catch {
      /* empty */
    }

    return {
      result: errorMessage,
      sanitized: undefined,
    };
  }

