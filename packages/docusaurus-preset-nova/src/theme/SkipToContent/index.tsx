import { SkipToContentLink } from '@docusaurus/theme-common';

/**
 * Theme - Skip To Content - Skip To Content.
 *
 * Renders the Docusaurus skip-to-content accessibility
 * link without any framework-specific styling, allowing keyboard
 * users to bypass navigation and jump to main content.
 *
 * @constructor
 *
 * @since 0.15.0
 */
function SkipToContent() {
  return (
    <SkipToContentLink />
  );
}

export default SkipToContent;
