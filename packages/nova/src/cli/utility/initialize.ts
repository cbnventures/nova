  /**
   * CLI - Utility - Initialize - Prompt Workspaces.
   *
   * Discovers all package.json paths in the monorepo and lets the user assign a role, policy,
   * name, and recipes to each workspace directory.
   *
   * @param {Cli_Utility_Initialize_Runner_PromptWorkspaces_Config} config - Config.
   *
   * @private
   *
   * @returns {Cli_Utility_Initialize_Runner_PromptWorkspaces_Returns}
   *
   * @since 0.11.0
   */
  private static async promptWorkspaces(config: Cli_Utility_Initialize_Runner_PromptWorkspaces_Config): Cli_Utility_Initialize_Runner_PromptWorkspaces_Returns {
    const workspaces: Cli_Utility_Initialize_Runner_PromptWorkspaces_Workspaces = (config['workspaces'] !== undefined) ? { ...(config['workspaces']) } : {};

    // The "run" command already guarantees we run in the project root (called "checkPath"), so we can traverse forward directly.
    const rawWorkspacePaths: Cli_Utility_Initialize_Runner_PromptWorkspaces_RawWorkspacePaths = await discoverPathsWithFile('package.json', 'forward');
    const workspacePaths: Cli_Utility_Initialize_Runner_PromptWorkspaces_WorkspacePaths = rawWorkspacePaths.map((rawWorkspacePath) => {
      const currentWorkingDirectory: Cli_Utility_Initialize_Runner_PromptWorkspaces_CurrentWorkingDirectory = process.cwd();
      const relativePath: Cli_Utility_Initialize_Runner_PromptWorkspaces_RelativePath = relative(currentWorkingDirectory, rawWorkspacePath);

      if (relativePath === '') {
        return './';
      }

      return `./${relativePath.split(sep).join('/')}`;
    });

    Logger.customize({
      name: 'Runner.promptWorkspaces',
      purpose: 'paths',
    }).debug(workspacePaths);

    while (true) {
      const choices: Cli_Utility_Initialize_Runner_PromptWorkspaces_Choices = workspacePaths.map((workspacePath) => {
        const workspace: Cli_Utility_Initialize_Runner_PromptWorkspaces_Workspace = workspaces[workspacePath];
        const summaryParts: Cli_Utility_Initialize_Runner_PromptWorkspaces_SummaryParts = [];

        if (workspace !== undefined && workspace['name'] !== undefined) {
          summaryParts.push(workspace['name']);
        }

        if (workspace !== undefined && workspace['role'] !== undefined) {
          summaryParts.push(workspace['role']);
        }

        if (workspace !== undefined && workspace['policy'] !== undefined) {
          summaryParts.push(workspace['policy']);
        }

        return {
          title: workspacePath,
          description: (summaryParts.length > 0) ? summaryParts.join(' · ') : 'Not configured yet.',
          value: workspacePath,
        };
      });

      choices.push({
        title: 'Back',
        description: 'Return to the category selection.',
        value: 'back',
      });

      const menuOutput: Cli_Utility_Initialize_Runner_PromptWorkspaces_MenuOutput = await Runner.promptWithCancel<Cli_Utility_Initialize_Runner_PromptWorkspaces_MenuOutputKey, Cli_Utility_Initialize_Runner_PromptWorkspaces_MenuOutputValue>({
        type: 'select',
        name: 'workspacePath',
        message: 'Select a workspace to configure.',
        choices,
      });

      if (menuOutput['cancelled'] === true) {
        return 'back';
      }

      const menuOutputResult: Cli_Utility_Initialize_Runner_PromptWorkspaces_MenuOutputResult = menuOutput['result'];

      if (
        menuOutputResult.workspacePath === undefined
        || menuOutputResult.workspacePath === 'back'
      ) {
        return 'back';
      }

      const workspacePath: Cli_Utility_Initialize_Runner_PromptWorkspaces_WorkspacePath = menuOutputResult.workspacePath;

      const formResult: Cli_Utility_Initialize_Runner_PromptWorkspaces_FormResult = await Runner.promptWorkspacesForm({
        workspacePath,
        existingWorkspace: workspaces[workspacePath],
        projectSlug: (config['project'] !== undefined && config['project']['name'] !== undefined) ? config['project']['name']['slug'] : undefined,
      });

      if (formResult['action'] === 'back') {
        continue;
      }

      Reflect.set(workspaces, workspacePath, formResult['workspace']);

      Object.assign(config, { workspaces });

      Logger.customize({
        name: 'Runner.promptWorkspaces',
        purpose: 'updated',
        padTop: 1,
        padBottom: 1,
      }).info(`Updated workspace "${workspacePath}" → ${formResult['workspace']['name']} · ${formResult['workspace']['role']} · ${formResult['workspace']['policy']}`);
    }
  }

