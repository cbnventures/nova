    const validRoles: Cli_Utility_Initialize_Runner_PromptEntitiesForm_ValidRoles = [...libItemValidEntityRoles] as Cli_Utility_Initialize_Runner_PromptEntitiesForm_ValidRoles;

    const existingName: Cli_Utility_Initialize_Runner_PromptEntitiesForm_ExistingName = (entity !== undefined && typeof entity['name'] === 'string') ? entity['name'] : '';
